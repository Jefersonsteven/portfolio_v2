'client:only'
const switchOpenMenu = document.querySelector('.switch-open-menu');
const switchCloseMenu = document.querySelector('.switch-close-menu');
const menu = document.querySelector('.menu');


if(switchOpenMenu && switchCloseMenu && menu) {
  switchOpenMenu.addEventListener('click', () => {
    menu.classList.add('active');
  })

  switchCloseMenu.addEventListener('click', () => {
    menu.classList.remove('active');
  })
}


